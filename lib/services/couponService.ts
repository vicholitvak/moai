  // Validate and apply coupon to order
  static async validateCoupon(
    code: string, 
    userId: string, 
    orderData: {
      subtotal: number;
      categories: string[];
      cookIds: string[];
      isFirstOrder: boolean;
    }
  ): Promise<{
    isValid: boolean;
    coupon?: Coupon;
    discount?: number;
    error?: string;
  }> {
    try {
      // Get coupon by code
      const couponQuery = query(
        collection(db, this.COLLECTION_COUPONS),
        where('code', '==', code.toUpperCase())
      );
      
      const couponSnapshot = await getDocs(couponQuery);
      
      if (couponSnapshot.empty) {
        return { isValid: false, error: 'Cup贸n no encontrado' };
      }

      const couponDoc = couponSnapshot.docs[0];
      const coupon = { id: couponDoc.id, ...couponDoc.data() } as Coupon;

      // Validate coupon
      const validation = await this.validateCouponRules(coupon, userId, orderData);
      if (!validation.isValid) {
        return validation;
      }

      // Calculate discount
      const discount = this.calculateDiscount(coupon, orderData.subtotal);

      return {
        isValid: true,
        coupon,
        discount
      };

    } catch (error) {
      console.error('Error validating coupon:', error);
      return { isValid: false, error: 'Error validando el cup贸n' };
    }
  }

  // Apply coupon to order
  static async applyCoupon(
    couponId: string,
    userId: string,
    orderId: string,
    discountAmount: number
  ): Promise<void> {
    try {
      // Update coupon usage count
      await updateDoc(doc(db, this.COLLECTION_COUPONS, couponId), {
        usageCount: increment(1)
      });

      // Create user coupon record
      const userCoupon: Omit<UserCoupon, 'id'> = {
        userId,
        couponId,
        couponCode: '',
        usedAt: Timestamp.now(),
        orderId,
        discountAmount,
        isUsed: true,
        assignedAt: Timestamp.now(),
        expiresAt: Timestamp.now()
      };

      await addDoc(collection(db, this.COLLECTION_USER_COUPONS), userCoupon);

    } catch (error) {
      console.error('Error applying coupon:', error);
      throw error;
    }
  }

